function [obj] = possumFunc(pos)
% VERI OKUMA ISLEMI
% data = xlsread('SupermarketSalesDataSet','Sayfa1');
load('possum.mat');
dataLength = length(possum);
totalError = 0;

    for i=1:dataLength
        realObj = possum(i,12);
        guessObj(i) = pos(1)*possum(i,1)+...
            pos(2)*possum(i,2)+...
            pos(3)*possum(i,3)+...
            pos(4)*possum(i,4)+...
            pos(5)*possum(i,5)+...
            pos(6)*possum(i,6)+...
            pos(7)*possum(i,7)+...
            pos(8)*possum(i,8)+...
            pos(9)*possum(i,9)+...
            pos(10)*possum(i,10)+...
            pos(11)*possum(i,11)+...
            pos(12);
        
        difference = realObj - guessObj(i);
        objectValue(i, 1) = difference * difference;
        totalError = totalError + objectValue(i, 1);
        
    end

    obj = totalError;

end


