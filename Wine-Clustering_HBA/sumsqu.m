    for i=1:dataLength
        realObj = wineData(i,12);
        guessObj(i) = pos(1)*wineData(i,1)+...
            pos(2)*wineData(i,2)+...
            pos(3)*wineData(i,3)+...
            pos(4)*wineData(i,4)+...
